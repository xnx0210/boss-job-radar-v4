# ---- 合格公司评分 ----

def _score_job(job: dict, plus_words: str, minus_words: str):
    """对岗位进行加分/扣分评分。返回 (score, plus_matches, minus_matches)。"""
    text = ' '.join(filter(None, [
        job.get('title', '') or job.get('job_title', ''),
        job.get('company', ''),
        job.get('description', ''),
    ])).lower()
    score = 0
    plus_matches = []
    minus_matches = []
    if plus_words:
        for w in plus_words.split(','):
            w = w.strip().lower()
            if w and w in text:
                score += 10
                plus_matches.append(w)
    if minus_words:
        for w in minus_words.split(','):
            w = w.strip().lower()
            if w and w in text:
                score -= 10
                minus_matches.append(w)
    return score, plus_matches, minus_matches


def _fetch_company_capital_via_api(company_id: str, cookies: dict) -> int:
    """通过 BOSS 公开 API 获取公司注册资本（万元），失败返回 -1。"""
    if not company_id or not cookies:
        return -1
    try:
        url = f"https://www.zhipin.com/wapi/zpgeek/company/info.json?companyId={company_id}"
        headers = {
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64; rv:125.0) Gecko/20100101 Firefox/125.0",
            "Referer": "https://www.zhipin.com/",
            "Cookie": "; ".join([f"{k}={v}" for k, v in cookies.items()]),
            "Accept": "application/json, text/plain, */*",
        }
        resp = httpx.get(url, headers=headers, timeout=10)
        if resp.status_code != 200:
            return -1
        data = resp.json()
        info = data.get("data", {}) or {}
        if isinstance(info, dict) and "companyInfo" in info:
            info = info["companyInfo"]
        capital = info.get("registeredCapital") or info.get("registered_capital") or ""
        if capital:
            m = re.search(r"(\d+(?:\.\d+)?)", str(capital))
            if m:
                return int(float(m.group(1)))
        return -1
    except Exception:
        return -1


