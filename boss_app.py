@app.post("/api/jobs/re-evaluate-qualified")
async def re_evaluate_qualified():
    """扫描数据库所有已有JD的岗位，按当前评分规则和注册资本门槛重新评估，加入合格公司。"""
    if not automation or automation.page is None:
        raise HTTPException(status_code=503, detail="浏览器未启动")

    from boss_state import get_db

    _plus = get_setting("score_plus_words", "")
    _minus = get_setting("score_minus_words", "")
    _th_str = get_setting("score_threshold", "10")
    try:
        _th = int(_th_str)
    except ValueError:
        _th = 10
    _min_cap_str = get_setting("min_registered_capital", "0")
    try:
        _min_cap = int(_min_cap_str)
    except ValueError:
        _min_cap = 0

    db = get_db()
    rows = db.execute(
        "SELECT * FROM applications WHERE description != '' AND description IS NOT NULL"
    ).fetchall()

    existing_urls = set()
    for q in list_qualified_companies(limit=9999):
        if q.get("job_url"):
            existing_urls.add(q["job_url"])

    _cookies = {}
    try:
        _cks = await _run_pw(lambda: automation.page.context.cookies())
        _cookies = {_c["name"]: _c["value"] for _c in _cks}
    except Exception:
        pass

    added = 0
    for row in rows:
        j = dict(row)
        job_url = j.get("job_url", "")
        if not job_url or job_url in existing_urls:
            continue

        _s, _p_matches, _m_matches = _score_job(j, _plus, _minus)
        if _s < _th:
            continue

        _cap_ok = True
        if _min_cap > 0 and j.get("company_id"):
            _cap = _fetch_company_capital_via_api(j["company_id"], _cookies)
            if _cap >= 0:
                _cap_ok = _cap >= _min_cap

        if _cap_ok:
            _detail = json.dumps({"plus": _p_matches, "minus": _m_matches}, ensure_ascii=False)
            result = add_qualified_company(
                job_url=job_url,
                job_title=j.get("job_title", ""),
                company=j.get("company", ""),
                salary=j.get("salary", ""),
                city=j.get("city", ""),
                score=_s,
                score_detail=_detail,
                description=j.get("description", ""),
            )
            if result:
                added += 1

    return {"status": "ok", "added": added, "total_checked": len(rows)}


