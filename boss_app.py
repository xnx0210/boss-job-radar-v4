    global browser_sync_lock
    conv = get_conversation(conv_id)
    if not conv:
        raise HTTPException(status_code=404, detail="会话不存在")
    if not automation or automation.page is None:
        return {
            "success": False,
            "message": "浏览器未启动",
            "messages": _clean_messages_for_web(get_messages(conv_id, 100)),
        }

    hr_name = conv.get("hr_name", "")
    if not hr_name:
        raise HTTPException(status_code=400, detail="会话缺少HR姓名")

    if browser_sync_lock is None:
        browser_sync_lock = asyncio.Lock()
    if browser_sync_lock.locked():
        return {
            "success": False,
            "message": "浏览器正忙，先显示缓存",
            "messages": _clean_messages_for_web(get_messages(conv_id, 100)),
        }

    try:
        async with browser_sync_lock:
            opened = await asyncio.wait_for(_run_pw(automation.open_conversation_by_name, hr_name), timeout=8)
            if not opened:
                return {
                    "success": False,
                    "message": f"无法打开 {hr_name} 的会话",
                    "messages": _clean_messages_for_web(get_messages(conv_id, 100)),
                }

            live_messages = await asyncio.wait_for(_run_pw(automation.read_visible_messages), timeout=5)
            online_status = await asyncio.wait_for(_run_pw(automation.read_chat_online_status), timeout=3)
            header_info = await asyncio.wait_for(_run_pw(automation.read_chat_header_info), timeout=3)
            _jt = (header_info.get('jobTitle') or '').strip()
            _sal = (header_info.get('salary') or '').strip()
            _city = (header_info.get('city') or '').strip()
            _parts = [p for p in [_jt, _sal, _city] if p]
            _job_str = ' · '.join(_parts) if _parts else '-'
            print(f"  [同步] {hr_name}: {len(live_messages or [])}条消息, 在线={online_status}, 岗位={_job_str}")
            if live_messages:
                replace_conversation_messages(conv_id, live_messages)
                # 更新在线状态和公司信息
                try:
                    from boss_state import get_db
                    db = get_db()
                    updates = []
                    params = []
                    if online_status:
                        updates.append("online_status=?")
                        params.append(online_status)
                    else:
                        # 对方离线时清空在线状态
                        updates.append("online_status=?")
                        params.append("")
                    if _jt:
                        updates.append("job_title=?")
                        params.append(_jt)
                    if _sal:
                        updates.append("salary=?")
                        params.append(_sal)
                    if _city:
                        updates.append("city=?")
                        params.append(_city)
                    if updates:
                        params.append(conv_id)
                        db.execute(f"UPDATE conversations SET {', '.join(updates)} WHERE id=?", params)
                        db.commit()
                        print(f"  [同步] 已更新: {updates}")
                except Exception:
                    pass
                last = live_messages[-1]
                # 过滤BOSS系统通知，不算真正的HR回复
                _sys_prefixes = ("你与该职位竞争者PK情况","竞争力分析","BOSS安全提示","系统消息","沟通分析","今日推荐","该Boss已查看了你的简历")
                last_content = (last.get("content") or "").strip()
                is_system = last.get("sender") == "hr" and len(last_content) <= 80 and any(last_content.startswith(p) for p in _sys_prefixes)
                if not is_system:
                    update_conversation_last_message(conv_id, last.get("content", ""), last.get("sender", "hr"))
    except asyncio.TimeoutError:
        return {
            "success": False,
            "message": "同步超时，先显示缓存",
            "messages": _clean_messages_for_web(get_messages(conv_id, 100)),
        }

    return {
        "success": True,
        "messages": _clean_messages_for_web(get_messages(conv_id, 100)),
    }


